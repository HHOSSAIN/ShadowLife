/** This stores some final constants which is used to represent directions */
public class Direction {
    /** This constant stores a value to represent UP direction */
    public static final int UP = 0;

    /** This constant stores a value to represent DOWN direction */
    public static final int DOWN = 1;

    /** This constant stores a value to represent LEFT direction */
    public static final int LEFT = 2;

    /** This constant stores a value to represent RIGHT direction */
    public static final int RIGHT = 3;

}
